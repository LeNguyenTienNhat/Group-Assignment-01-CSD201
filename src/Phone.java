public class Phone {
    int id;
    String name;
    int price;
    int amount;
    int year;

    public Phone() {
    }

    public Phone(int id, String name, int price, int amount, int year) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.amount = amount;
        this.year = year;
    }

}
